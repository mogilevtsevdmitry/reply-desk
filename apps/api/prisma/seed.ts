async function main(): Promise<void> {
  // Идемпотентность: сносим прошлые demo-данные (каскад уберёт отзывы/генерации/счётчики).
  const existing = await prisma.user.findUnique({ where: { email: DEMO_EMAIL } });
  if (existing) {
    if (existing.companyId) {
      await prisma.user.update({ where: { id: existing.id }, data: { companyId: null } });
      await prisma.company.delete({ where: { id: existing.companyId } }).catch(() => undefined);
    }
    await prisma.user.delete({ where: { id: existing.id } });
  }

  const company = await prisma.company.create({
    data: {
      name: 'Студия красоты «Лето»',
      niche: 'SALON',
      plan: 'FREE',
      toneOfVoice: {
        tone: 'neutral',
        examples: [
          'Анна, спасибо, что рассказали. Мастер Ольга уже знает о ситуации — приходите в четверг, поправим укладку за наш счёт.',
          'Рады, что окрашивание понравилось! Ждём вас на поддержание оттенка — администратор подберёт удобное время.',
        ],
        avoid: 'Не обещать возврат денег без проверки и не упоминать скидки в публичных ответах.',
      },
    },
  });

  await prisma.user.create({
    data: {
      email: DEMO_EMAIL,
      passwordHash: hashSync(DEMO_PASSWORD, BCRYPT_COST),
      companyId: company.id,
    },
  });

  const createdIds: string[] = [];
  for (const seed of REVIEWS) {
    const similarIds = seed.similarIdx
      .map((i) => createdIds[i])
      .filter((id): id is string => id !== undefined);
    const payload = GenerationPayloadSchema.parse(seed.payload(similarIds));

    const review = await prisma.review.create({
      data: {
        companyId: company.id,
        source: seed.source,
        rating: seed.rating,
        rawText: seed.rawText,
        category: payload.classification.category,
        severity: payload.classification.severity,
        isFakeSusp: payload.classification.fakeSuspicion.flag,
        createdAt: seed.createdAt,
        generation: {
          create: {
            status: 'DONE',
            publicReplies: payload.publicReplies as Prisma.InputJsonObject,
            internalTask: payload.internalTask as Prisma.InputJsonObject,
            classification: payload.classification as Prisma.InputJsonObject,
            winback: payload.winback as Prisma.InputJsonObject,
            tokensUsed: 1850,
            createdAt: seed.createdAt,
          },
        },
      },
    });
    createdIds.push(review.id);
  }

  // 5 генераций уже использовано в текущем периоде (FREE: 10/мес).
  await prisma.usageCounter.create({
    data: { companyId: company.id, period: currentPeriod(), used: REVIEWS.length },
  });

  // Тексты отзывов не логируем — только количества и id компании.
  console.log(
    `Seed готов: ${DEMO_EMAIL} / ${DEMO_PASSWORD}, компания ${company.id}, отзывов: ${createdIds.length}, used=${REVIEWS.length}`,
  );
}

main()
  .catch((e) => {
    console.error('Seed упал:', e instanceof Error ? e.message : e);
    process.exitCode = 1;
  })
  .finally(() => prisma.$disconnect());
